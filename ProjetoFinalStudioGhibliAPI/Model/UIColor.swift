import Foundation
import UIKit

extension UIColor{
    
    class func meuRosa() -> UIColor {
        return UIColor(red: 255/255.0, green: 192/255.0, blue: 203/255.0, alpha: 0.6)
    }
    
    class func meuRoxo() -> UIColor {
        return UIColor(red: 147/250.0, green: 112/250.0, blue: 219/250.0, alpha: 0.8)
    }
    
    class func meuCinza() -> UIColor {
        return UIColor(red: 28/250.0, green: 28/250.0, blue: 28/250.0, alpha: 1.0)
    }
}
